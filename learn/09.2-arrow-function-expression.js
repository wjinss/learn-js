// ------------------------
// 화살표 함수 표현식 (arrow-function-expression)
// ------------------------

// 함수를 만드는 방법 3가지(함수 정의)

// 1. 함수 선언문
function showMeTheMoney() {
  console.log("show me the money");
}
showMeTheMoney();
// 2. 함수 표현식
let lookAtMe = function () {
  console.log("look at me");
};
lookAtMe();
// 3. 화살표 함수
let readABook = () => {
  console.log("read a book");
};
readABook();
